# Tipe Data int (Bilangan Bulat)
angka_bulat = 10

# Tipe Data float (Bilangan Desimal)
angka_desimal = 3.14

# Tipe Data string (Teks)
teks = "Halo, ini adalah contoh teks."

# Tipe Data boolean (Boolean)
benar = True
salah = False

print("Tipe Data int (Bilangan Bulat):", angka_bulat)
print("Tipe Data float (Bilangan Desimal):", angka_desimal)
print("Tipe Data string (Teks):", teks)
print("Tipe Data boolean (Boolean) Benar:", benar)
print("Tipe Data boolean (Boolean) Salah:", salah)

# Operasi Matematika
angka1 = 5
angka2 = 3
hasil_penjumlahan = angka1 + angka2
hasil_pengurangan = angka1 - angka2
hasil_perkalian = angka1 * angka2
hasil_pembagian = angka1 / angka2

print("\nOperasi Matematika:")
print("Penjumlahan:", hasil_penjumlahan)
print("Pengurangan:", hasil_pengurangan)
print("Perkalian:", hasil_perkalian)
print("Pembagian:", hasil_pembagian)

# Operasi String
teks1 = "Halo, "
teks2 = "Dunia!"
hasil_gabungan = teks1 + teks2

print("\nOperasi String:")
print("Hasil Gabungan:", hasil_gabungan)

# Operasi Logika
x = True
y = False

logika_and = x and y
logika_or = x or y
logika_not_x = not x

print("\nOperasi Logika:")
print("Logika AND:", logika_and)
print("Logika OR:", logika_or)
print("Logika NOT x:", logika_not_x)
